export function findDreamSymbols(query: string): DreamSymbol[] {
  const lowerQuery = query.toLowerCase();
  return DREAM_SYMBOLS.filter(
    (s) =>
      s.symbol.toLowerCase().includes(lowerQuery) ||
      s.traditionalMeaning.toLowerCase().includes(lowerQuery) ||
      s.spiritualMeaning.toLowerCase().includes(lowerQuery) ||
      s.psychologicalMeaning.toLowerCase().includes(lowerQuery) ||
      s.advice.toLowerCase().includes(lowerQuery)
  );
}

export function getDreamSymbolsByCategory(category: DreamCategory): DreamSymbol[] {
  return DREAM_SYMBOLS.filter((s) => s.category === category);
}
